# Add stage_name based on funnel percentages (final stage a deal reaches)
# These percentages are from the diagram and represent the cumulative proportion
# of deals that reach at least that stage, starting from 100% Leads.
# To assign a *single final stage* to each deal, we use the differences:
# - Сделка: 11% (of total deals)
# - КП отправлено: 20% - 11% = 9%
# - Переговоры: 40% - 20% = 20%
# - Контакт установлен: 70% - 40% = 30%
# - Лиды: 100% - 70% = 30%

def assign_stage(val):
    if val <= 0.11:
        return 'Сделка'
    elif val <= 0.20: # 0.11 < val <= 0.20
        return 'КП отправлено'
    elif val <= 0.40: # 0.20 < val <= 0.40
        return 'Переговоры'
    elif val <= 0.70: # 0.40 < val <= 0.70
        return 'Контакт установлен'
    else: # 0.70 < val <= 1.0
        return 'Лиды'

